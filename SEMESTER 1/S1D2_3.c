#include<stdio.h>
#include<math.h>
int main()
{
	int n,sqroot;
	printf("Enter the number:\n");
	scanf("%d",&n);
	
	sqroot = sqrt(n);
	
	printf("Square root:\n%d\n",sqroot);
	
	return 0;
}

